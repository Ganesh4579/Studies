public class meth {
    public  static double mul(double x, double y) {
        return x * y;
    }

    public  static int mul(int x, int y) {
        return x * y;
    }

    public static void main(String[] args) {
        System.out.println(mul(4, 5));
        System.out.println(mul(3.1, 2.8));
    }

}
 